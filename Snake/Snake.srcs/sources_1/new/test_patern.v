`timescale 1ns / 1ps
//////////////////////////////////////////////////////////////////////////////////
//
//Visible area X = 0-1367
//Visible area Y = 0-767
//
//X
//  0 - 455
//  456 - 911
//  912 - 1367
//
//Y
//  0 - 255
//  256 - 511
//  512 - 767
//
//////////////////////////////////////////////////////////////////////////////////


module test_patern(
    input [10:0] X,
    input [9:0] Y,
    output [11:0] colour
    );
    
    wire X0 = (X <= 455);
    wire X1 = ((X >= 456) && (X <= 911));
    wire X2 = ((X >= 912) && (X <= 1367));
    
    wire Y0 = (Y <= 255);
    wire Y1 = ((Y >= 256) && (Y <= 511));
    wire Y2 = ((Y >= 512) && (Y <= 767));
    
    
    assign colour = (X0 && Y0) ? 12'b000000000000: //0 0 0
                    (X1 && Y0) ? 12'b111100000000: //1 0 0
                    (X2 && Y0) ? 12'b000011110000: //0 1 0
                    (X0 && Y1) ? 12'b111111110000: //1 1 0
                    (X1 && Y1) ? 12'b000000001111: //0 0 1
                    (X2 && Y1) ? 12'b111100001111: //1 0 1
                    (X0 && Y2) ? 12'b000011111111: //0 1 1
                    (X1 && Y2) ? 12'b111111111111: //1 1 1
                                 12'b010001000100; //~0.5 ~0.5 ~0.5
    
endmodule
